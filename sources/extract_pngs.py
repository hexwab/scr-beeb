def unpack_c64_1bpp(x):
    pixels=[]
    mask=128
    while mask!=0:
        pixels.append(1 if (x&mask)!=0 else 0)
        mask>>=1
    return pixels

def unpack_c64_2bpp(x): return [x>>6&3,x>>4&3,x>>2&3,x>>0&3]

def pack_beeb(p):
    return ((p[0]>>1&1)<<7|
            (p[1]>>1&1)<<6|
            (p[2]>>1&1)<<5|
            (p[3]>>1&1)<<4|
            (p[0]&1)<<3|
            (p[1]&1)<<2|
            (p[2]&1)<<1|
            (p[3]&1)<<0)

